package com.smartvision.svplayer.data.tmdb

import com.smartvision.svplayer.data.local.entity.TmdbMovieMetadataEntity
import com.smartvision.svplayer.data.local.entity.TmdbSeriesMetadataEntity

data class TmdbMovieMetadata(
    val tmdbId: Int,
    val language: String,
    val title: String,
    val originalTitle: String?,
    val overview: String?,
    val posterUrl: String?,
    val backdropUrl: String?,
    val logoUrl: String?,
    val releaseDate: String?,
    val runtimeMinutes: Int?,
    val genres: String?,
    val voteAverage: Double?,
    val voteCount: Int?,
    val popularity: Double?,
    val cast: String?,
    val director: String?,
    val trailerKey: String?,
    val collectionName: String?,
    val certification: String?,
    val providersSummary: String?,
    val adult: Boolean,
)

data class TmdbSeriesMetadata(
    val tmdbId: Int,
    val language: String,
    val name: String,
    val originalName: String?,
    val overview: String?,
    val posterUrl: String?,
    val backdropUrl: String?,
    val logoUrl: String?,
    val firstAirDate: String?,
    val episodeRunTimeMinutes: Int?,
    val genres: String?,
    val voteAverage: Double?,
    val voteCount: Int?,
    val popularity: Double?,
    val cast: String?,
    val createdBy: String?,
    val trailerKey: String?,
    val certification: String?,
    val providersSummary: String?,
    val adult: Boolean,
)

fun TmdbMovieMetadataEntity.toTmdbMovieMetadata(): TmdbMovieMetadata =
    TmdbMovieMetadata(
        tmdbId = tmdbId,
        language = language,
        title = title,
        originalTitle = originalTitle,
        overview = overview,
        posterUrl = posterUrl,
        backdropUrl = backdropUrl,
        logoUrl = logoUrl,
        releaseDate = releaseDate,
        runtimeMinutes = runtimeMinutes,
        genres = genres,
        voteAverage = voteAverage,
        voteCount = voteCount,
        popularity = popularity,
        cast = cast,
        director = director,
        trailerKey = trailerKey,
        collectionName = collectionName,
        certification = certification,
        providersSummary = providersSummary,
        adult = adult,
    )

fun TmdbSeriesMetadataEntity.toTmdbSeriesMetadata(): TmdbSeriesMetadata =
    TmdbSeriesMetadata(
        tmdbId = tmdbId,
        language = language,
        name = name,
        originalName = originalName,
        overview = overview,
        posterUrl = posterUrl,
        backdropUrl = backdropUrl,
